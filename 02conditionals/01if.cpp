// If Else statements!
// False = 0; True = any number except 0

#include <iostream>

using namespace std;

int main() 
{
  int x, y;
  cout << "Enter 2 Numbers" << endl;
  cin >> x >> y;
  if (x > y)
  {
    cout << "Max is " << x << endl;
  }
  else
  {
    cout << "Max is " << y << endl;
  }
  
}